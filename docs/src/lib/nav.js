export function prevNext(currentHref) {
  const all = NAV.flatMap(s => s.items)
  const idx = all.findIndex(i => i.href === currentHref)
  return {
    prev: idx > 0             ? all[idx - 1] : null,
    next: idx < all.length - 1 ? all[idx + 1] : null,
  }
}
